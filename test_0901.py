import numpy as np

# 旋转矩阵 R
R = np.array([
    [-0.0264814146660713, -0.999649386885150, 0.00173906523035761],
    [0.0121395165905959, -0.00206233611926795, -0.999925722855674],
    [ 0.999576628994339, -0.0264570578759927, 0.0121885681396033]
])

# 平移向量 T
T = np.array([0.0659530032036776, 0.108374858163385, 0.0699178702997177]).reshape(3, 1)

# 组装外参矩阵 [R | T]
extrinsic = np.hstack((R, T))

# 扩展为 4x4 齐次矩阵
extrinsic_h = np.vstack((extrinsic, [0, 0, 0, 1]))

print("Extrinsic matrix:\n", extrinsic_h)
